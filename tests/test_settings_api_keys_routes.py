# ---------- POST /settings/api-keys (create) ----------


def test_create_api_key_happy_path(reload_app, tmp_path, monkeypatch):
    monkeypatch.setenv("CERT_WATCH_COOKIE_SECURE", "0")
    monkeypatch.setenv("CERT_WATCH_CSRF_DISABLED", "1")
    db = _seed_local_admin(tmp_path)
    app_mod = reload_app()
    with TestClient(app_mod.app) as client:
        _login_admin(client, monkeypatch)
        r = client.post(
            "/settings/api-keys",
            data={"name": "deploy-key", "scope": "write"},
            follow_redirects=False,
        )
    assert r.status_code == 200
    assert "deploy-key" in r.text
    assert "cwk_" in r.text

    repo = SqliteApiKeyRepository(db)
    keys = repo.list_keys()
    assert len(keys) == 1
    assert keys[0].name == "deploy-key"
    assert keys[0].scope == "write"
    assert keys[0].revoked is False

    rows = _read_audit_rows(db)
    assert len(rows) >= 1
    audit = rows[0]
    assert audit["actor"] == "admin"
    assert audit["action"] == "api_key.create"
    assert audit["target_type"] == "api_key"
    assert audit["target_id"] == keys[0].id
    detail = json.loads(audit["detail"])
    assert detail["name"] == "deploy-key"
    assert detail["scope"] == "write"
    # Security property: the raw token must never appear in the audit row.
    assert "cwk_" not in audit["detail"]
    assert audit["source_ip"] is not None


