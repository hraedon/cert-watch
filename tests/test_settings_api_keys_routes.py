def test_get_api_keys_authenticated_admin_redirects(reload_app, tmp_path, monkeypatch):
    monkeypatch.setenv("CERT_WATCH_COOKIE_SECURE", "0")
    monkeypatch.setenv("CERT_WATCH_CSRF_DISABLED", "1")
    _seed_local_admin(tmp_path)
