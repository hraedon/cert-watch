# ---------- POST /settings/api-keys/{key_id}/revoke ----------


def test_revoke_api_key_happy_path(reload_app, tmp_path, monkeypatch):
    monkeypatch.setenv("CERT_WATCH_COOKIE_SECURE", "0")
    monkeypatch.setenv("CERT_WATCH_CSRF_DISABLED", "1")
    db = _seed_local_admin(tmp_path)
    app_mod = reload_app()

    repo = SqliteApiKeyRepository(db)
    entry, raw_token = repo.create_key("to-revoke", "admin")

    with TestClient(app_mod.app) as client:
        _login_admin(client, monkeypatch)
        r = client.post(
            f"/settings/api-keys/{entry.id}/revoke",
            follow_redirects=False,
        )
    assert r.status_code == 303
    assert "api-keys" in r.headers["location"]

    assert repo.verify_key(raw_token) is None

    rows = _read_audit_rows(db)
    assert len(rows) >= 1
    audit = rows[0]
    assert audit["actor"] == "admin"
    assert audit["action"] == "api_key.revoke"
    assert audit["target_type"] == "api_key"
    assert audit["target_id"] == entry.id


